class Profile {
  final String name;
  final int age;
  final String bio;
  final String imageUrl;
  final String job;
  final String school;
  final String location;
  final String personality;
  final String zodiac;

  Profile({
    required this.name,
    required this.age,
    required this.bio,
    required this.imageUrl,
    required this.job,
    required this.school,
    required this.location,
    required this.personality,
    required this.zodiac,
  });
}
